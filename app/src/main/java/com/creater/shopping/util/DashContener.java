package com.creater.shopping.util;

public class DashContener {
    String value;
    int img;

    public DashContener(String value, int img) {
        this.value = value;
        this.img = img;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
